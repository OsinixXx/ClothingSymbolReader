import time

import cv2		as cv
import numpy		as np

import libalx.stdio	as alx


################################################################################
#	global variables						       #
################################################################################


################################################################################
#	functions							       #
################################################################################
def img_get():
	cam		= cv.VideoCapture(0);
	if not cam.isOpened():
		raise Exception("Couldn't open cam.\n");

	_, frame	= cam.read();
	cam.release();

	return	frame;

def wait_for_ESC():
	ESC	= 27;
	while (True):
		k	= cv.waitKey(5) & 0xFF;
		if k == ESC:
			break;


################################################################################
#	main								       #
################################################################################
def main():

	alx.printf("Hello, world!\n");
	alx.printf("We have %i days to finish this\n", 4);

	img	= img_get();

	cv.namedWindow("img");
	cv.imshow("img", img);

	wait_for_ESC();

	cv.destroyAllWindows();


################################################################################
#	run								       #
################################################################################
if __name__ == "__main__":
	main();


